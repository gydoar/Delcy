			<h3>VARISES</h3>

			<p>

				<p>Las várices son venas dilatadas que se inflaman y se elevan a la superficie de la piel. Pueden ser de un color morado o azul oscuro y parecer estar torcidas y abultadas. Las várices se encuentran comúnmente en las partes posteriores de las pantorrillas o en la cara interna de la pierna.</p>

				<p>Se desarrollan cuando las válvulas venosas que permiten que la sangre fluya hacia el corazón dejan de funcionar adecuadamente. 
				Como resultado, la sangre se acumula en las venas y provoca las dilataciones.

				Entre los más significativos según avanza la enfermedad, destacan:
				</p>
				
				<ul>
					<li>Visualización de la red venosa de las piernas. En general, pueden verse varices en cara antero externa de muslos, detrás de las rodillas, y en cara interna de piernas, pero al principio no suelen aparecer otros síntomas.</li>
					<li>Pesadez y cansancio en las piernas. Sobre todo cuando se está mucho tiempo inmóvil de pie, y a última hora del día.</li>
					<li>Dolor. De intensidad variable según las personas. Normalmente se localiza en los trayectos de las venas afectadas, principalmente tobillo y pantorrilla. Puede empezar o aumentar con un simple roce, o un golpe de poca importancia.
					Calambres. Principalmente nocturnos.</li>
					<li>Hormigueos. Especialmente cuando las piernas permanecen mucho tiempo en la misma postura, por ejemplo en el cine o durante viajes en autocar o avión.</li>
					<li>Sensación de calor o picores y escozores. Principalmente en tobillo y dorso del pie. Debe evitarse el rascado, pues pueden hacerse heridas con facilidad, al ser la piel más débil por la mala circulación y, también por este motivo, infectarse con facilidad.</li>
					<li>Hinchazón o edema de los pies y tobillos. Aparece, según avanza la enfermedad.
				Cambios de coloración en la piel. Manchas parduscas o violáceas.</li>
				</ul>
		
