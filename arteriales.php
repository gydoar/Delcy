			<h3>ARTERIOSCLEROSIS</h3>
			<p>
				<p><img src="images/arteriosclerosis.png" alt=""></p>
				<p>Arteriosclerosis es una enfermedad de los vasos sanguíneos producida por un acumulo de colesterol, que hace que la sangre circule con mayor dificultad por las mismas y aumente el riesgo de producir obstrucciones. Esta enfermedad se desarrolla lentamente, puede comenzar en edades.</p>

				<h4>Principales Síntomas</h4>

				<p>Los síntomas que usted tendrá dependerán de cuáles arterias se encuentran gravemente obstruidas y qué parte del cuerpo resulta afectada por la reducción en el flujo de sangre. Normalmente no hay síntomas hasta que una o más arterias están tan obstruidas con placa, que se reduce drásticamente el flujo de sangre. Esta reducción en el flujo de sangre y oxígeno a algunas partes del cuerpo (como el corazón), se denomina isquemia y puede ocasionar dolor o molestias. Algunas personas no tienen síntomas hasta que se forma un coágulo de sangre que obstruye por completo una arteria ya estrechada, y causa un ataque cardiaco o derrame cerebral.</p>
					
				<ul>
					<li>Si resultan afectadas las arterias que transportan sangre al músculo del corazón, usted tendrá una enfermedad de las arterias coronarias (EAC). Puede tener un dolor en el pecho llamado angina de pecho, que se presenta cuando se esfuerza demasiado y desaparece cuando descansa. También podría tener un ataque cardiaco.</li>
					<li>Si resultan afectadas las arterias que transportan sangre al cerebro, usted tendrá una enfermedad cerebrovascular. Podría tener un ataque isquémico transitorio (AIT) o un derrame cerebral.</li>
					<li>Si resultan afectadas las arterias que transportan sangre a las piernas, usted tendrá una enfermedad arterial periférica (EAP). Cuando camina puede sentir un dolor denominado claudicación intermitente en los músculos de la pantorrilla o muslo. Este dolor desaparece cuando se detiene y descansa.</li>
				</ul>
				<p>Todas estas condiciones son graves y no deben ignorarse. También pueden bloquearse con placa las arterias que transportan sangre a los intestinos, riñones y otros órganos. Esto puede provocar una emergencia médica similar al ataque cardiaco o derrame cerebral. La aterosclerosis también puede ocasionar disfunción eréctil en los hombres.</p>

			</p>


			<h3>PIE DIABÉTICO</h3>
