			<p>
				<p>El pie diabético, según el Consenso Internacional sobre Pie Diabético es una infección, ulceración o destrucción de los tejidos profundos relacionados con alteraciones neurológicas y distintos grados de enfermedad vascular periférica en las extremidades inferiores que afecta a pacientes con diabetes mellitus.</p>

				<p>Es importante remarcar que no debe confundirse "pie diabético" con el pie de una persona diabética, ya que no todos los diabéticos desarrollan esta complicación que depende en gran medida del control que se tenga de la enfermedad, de los factores intrínsecos y ambientales asociados al paciente y en definitiva del estado evolutivo de la patología de base.</p>

				<h4>Principales Síntomas</h4>

				<p>Calambres que se agravan por la noche, parestesias y dolor que en ocasiones es muy intenso y se acompaña de hiperestesias, hasta el punto de que el paciente no tolera el roce de las sábanas.</p>

				<p>La pérdida de la sensibilidad vibratoria es uno de los síntomas más precoces. Hay una disminución o abolición de la sensibilidad propioceptiva.</p>

				<p>La hipoestesia (disminución de la sensibilidad) permite que se produzcan lesiones que son advertidas tardíamente por los pacientes.</p>

